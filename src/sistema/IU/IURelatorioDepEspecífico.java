    private void buttonExibirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonExibirActionPerformed
        // TODO add your handling code here:
        double ini =  Double.parseDouble(TextIni.getText());
        double fim  = Double.parseDouble(TextFim.getText());
        
        Controlador control = new Controlador();
        
        String d  = control.departamentosFaixaGasto(ini, fim);
        
        if ("".equals(d)) {
            textRelatorio.setText("");
            JOptionPane.showMessageDialog(null, "Faixa de gasto não encontrada nos departamentos");
        }else{
            textRelatorio.setText(d);
        }
    }//GEN-LAST:event_buttonExibirActionPerformed

